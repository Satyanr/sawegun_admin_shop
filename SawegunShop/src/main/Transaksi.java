    public void totalBiaya(){
        int jumlahBaris = tblbarang.getRowCount();
        int totalBiaya = 0;
        int hargaBarang;
        for (int i = 0; i < jumlahBaris; i++) {
            hargaBarang = Integer.parseInt(tblbarang.getValueAt(i, 4).toString());
            totalBiaya = totalBiaya +  hargaBarang;
        }
        txttbayar.setText(String.valueOf(totalBiaya));
    }
    
    private void autonumber(){
        try {
            Connection c = koneksi.getKoneksi();
            Statement s = c.createStatement();
            String sql = "SELECT * FROM transaksi ORDER BY id_transaksi DESC";
            ResultSet r = s.executeQuery(sql);
            if (r.next()) {
                String NoFaktur = r.getString("id_transaksi").substring(2);
                String BR = "" +(Integer.parseInt(NoFaktur)+1);
                String Nol = "";
                
                if (BR.length()==1) 
                    {Nol = "00";}
                else if(BR.length()==2)
                    {Nol = "0";}
                else if(BR.length()==3)
                    {Nol = "";}
                
                txttransid.setText("TR" + Nol + BR);  
            }else{
                txttransid.setText("TR001");
            }
            r.close();
            s.close();
        } catch (Exception e) {
            System.out.println("autonumber error");
        }
    }
    
