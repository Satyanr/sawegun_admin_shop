    private void txtbayarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtbayarActionPerformed
        // TODO add your handling code here:
        int total, bayar, kembalian;
        
        total = Integer.valueOf(txttbayar.getText());
        bayar = Integer.valueOf(txtbayar.getText());
        
        if (total > bayar) {
            JOptionPane.showMessageDialog(null, "Uang tidak cukup untuk melakukan pembayaran");
        } else {
            kembalian = bayar - total;
            txtkembalian.setText(String.valueOf(kembalian));
        }
    }//GEN-LAST:event_txtbayarActionPerformed

    private void btnsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsimpanActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) tblbarang.getModel();
        
        try {
            Connection c = koneksi.getKoneksi();
            int baris = tblbarang.getRowCount();
            
            for (int i = 0; i < baris; i++) {
                String sql = "INSERT INTO transaksi(id_transaksi, id_barang, nama_barang, jumlah, total_harga, date) VALUES('"
                        + tblbarang.getValueAt(i, 0) +"','"+ tblbarang.getValueAt(i, 1) +"','"+ tblbarang.getValueAt(i, 2) 
                        +"','"+ tblbarang.getValueAt(i, 3) +"','"+ tblbarang.getValueAt(i, 4) +"','"+ tblbarang.getValueAt(i, 5) 
                        +"')";
                PreparedStatement p = c.prepareStatement(sql);
                p.executeUpdate();
                p.close();
            }
        } catch (Exception e) {
            System.out.println("simpan penjualanrinci error");
        }
        clear();
        utama();
        autonumber();
        kosong();
        txttbayar.setText("Rp. 0");
    }//GEN-LAST:event_btnsimpanActionPerformed

